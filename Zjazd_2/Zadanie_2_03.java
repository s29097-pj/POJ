public class Zadanie_2_03 {
    public static void main(String[] args) {
        // deklaracja i inicjalizacja zmiennej typu short
        short s = 0;
        // deklaracja i inicjalizacja zmiennej typu int
        int i = 0;
        // deklaracja i inicjalizacja zmiennej typu long
        long l = 0;
        // deklaracja i inicjalizacja zmiennej typu float
        float f = 0.0f;
        // deklaracja i inicjalizacja zmiennej typu double
        double d = 0.0d;
        // deklaracja i inicjalizacja zmiennej typu char
        char c = '\u0000';
        // deklaracja zmiennej typu String
        String str = null;
        // deklaracja i inicjalizacja zmiennej typu boolean
        boolean b = false;

        // wyświetlenie komunikatu
        System.out.println("Default values for primitive types in Java:");
        // wyświetlenie wartości zmiennej typu short
        System.out.println("short s = " + s);
        // wyświetlenie wartości zmiennej typu int
        System.out.println("int i = " + i);
        // wyświetlenie wartości zmiennej typu long
        System.out.println("long l = " + l);
        // wyświetlenie wartości zmiennej typu float
        System.out.println("float f = " + f);
        // wyświetlenie wartości zmiennej typu double
        System.out.println("double d = " + d);
        // wyświetlenie wartości zmiennej typu char
        System.out.println("char c = " + c);
        // wyświetlenie wartości zmiennej typu String
        System.out.println("String str = " + str);
        // wyświetlenie wartości zmiennej typu boolean
        System.out.println("boolean b = " + b);
    }
}